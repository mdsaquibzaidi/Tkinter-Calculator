root.geometry("630x850")
root.title("calculator By zaidi")

srcvalue = StringVar()
srcvalue.set("")
screen = Entry(root, textvar=srcvalue,font="lucida 40 bold")
screen.pack(fill=X, ipadx=8,pady=10,padx=10)
f= Frame(root, bg="black")
button = Button(f,text="C", padx=18,pady=18,font="lucida 35 bold")
