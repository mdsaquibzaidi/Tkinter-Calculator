from tkinter import *
from tkinter import StringVar
global srcvalue
def click(event):
    text = event.widget.cget("text")
    print(text)
    if text == "=":
        if srcvalue.get().isdigit():
            value = int(srcvalue.get())
        else:
            value = eval(screen.get())

        srcvalue.set(value)
        srcvalue.update()
    elif text == "C":
        srcvalue.set("")
        srcvalue.update()
    else:
        srcvalue.set(srcvalue.get() + text)
        srcvalue.update()
